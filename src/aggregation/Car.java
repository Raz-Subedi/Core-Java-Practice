package aggregation;

public class Car {
    private String collor;
    private int price;
    private String model;

    public String getColor() {
        return collor;
    }

    public void setColor(String collor) {
        this.collor = collor;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }
}
